I2C_RESULT mag_read(void) 
{
BYTE i2cData[18];
I2C_RESULT i2c_result;
int i;
    i2c_result = I2CReadRegs(I2C1, MAG_ID, 0, i2cData, MAG_REG_SIZE);
    if(!i2c_result)
    {
        for(i=0; i<MAG_REG_SIZE; i++)
        {
            printf("%2d ", i2cData[i]);
        }
        printf("\n\r");
    }
    return i2c_result;
}


