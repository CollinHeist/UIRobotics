I2C_RESULT mag_init(void) 
{
I2C_RESULT i2c_result = I2C_SUCCESS;
BYTE cfg[2] = {MAG_CTRL_REG1, MAG_CFG1};
int len = 2;

    i2c_result = I2C_Write(I2C1, MAG_ID, cfg, &len);
    DelayMs(10);
    return i2c_result;
}

